#While loop

#Increment
number = 5
while number <= 10 :
    print(number)
    number += 1

#Decrement
num = 30
while num >= 25:
    print(num)
    num -= 1

#A for loop
for x in range(105,110,3):
    print("Number is", x)


devices = ["Laptop", "mouse" , "computer"]
print(devices)
for y in devices:
    print(y)